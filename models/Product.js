
class Product {    constructor(id, category, reviews, name, description, price, quantity, image) {
        this.id = id;
        this.category = category;
        this.reviews = reviews;
        this.name = name;
        this.description = description;
        this.price = price;
        this.quantity = quantity;
        this.image = image;
    }
}
export default Product;
